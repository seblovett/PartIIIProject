void FBufferStore(unsigned char character)
{
	unsigned char i,j;
	int char_p = character*FONT_SIZE;

	for (i=0; i<FONT_BIT_WIDTH; i++)
		for (j=0; j<FONT_BYTE_HIGHT; j++)
		{
			char_buf[i][j] = pgm_read_byte(&font[char_p]);
			char_p++;
		}
}

unsigned char FBufferRead(unsigned char column,unsigned char row)
{
	unsigned char read_pixel;

	if (row < 8)
	{
		read_pixel = (char_buf[column][0] >> (7-row)) & 0x01;
	}
	else
	{
		row = row - 8;
		read_pixel = (char_buf[column][1] >> (7-row)) & 0x01;
	}
	return (read_pixel);
}

